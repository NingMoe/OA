package com.oa.util;


/**
 * 消息传递类
 */
public class Message{
	
	private String msg;
	private Msgtype msgtype;
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public Msgtype getMsgtype() {
		return msgtype;
	}
	public void setMsgtype(Msgtype msgtype) {
		this.msgtype = msgtype;
	}
	
	
}
